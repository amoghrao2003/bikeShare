package com.voldy.Beans;

public class PaymentDetail {

	String user_name;
	String credit_cardno ;
	String cvv;
	String expiry;
	String name_onCard;

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getCredit_cardno() {
		return credit_cardno;
	}

	public void setCredit_cardno(String credit_cardno) {
		this.credit_cardno = credit_cardno;
	}

	public String getCvv() {
		return cvv;
	}

	public void setCvv(String cvv) {
		this.cvv = cvv;
	}

	public String getExpiry() {
		return expiry;
	}

	public void setExpiry(String expiry) {
		this.expiry = expiry;
	}

	public String getName_onCard() {
		return name_onCard;
	}

	public void setName_onCard(String name_onCard) {
		this.name_onCard = name_onCard;
	}
	
	
}
